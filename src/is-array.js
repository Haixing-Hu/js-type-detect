/**
 * Tests whether the specified value is an Array.
 *
 * @param {any} value
 *     the specified value.
 * @returns {boolean}
 *     `true` if the specified value is an Array; `false` otherwise.
 * @author Haixing Hu
 */
function isArray(value) {
  return Array.isArray(value);
}

export default isArray;
